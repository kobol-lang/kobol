    private fun line(s: String) {
        sb.append("    ".repeat(indent)).appendLine(s)
    }

    private fun blank() { sb.appendLine() }

    private val javaClassCache = HashMap<String, String>()

    private fun javaClass(kobolName: String): String {
        // CUSTOMER-REPORT → CustomerReport
        return javaClassCache.getOrPut(kobolName) {
            kobolName.split("-").joinToString("") { it.lowercase().replaceFirstChar { c -> c.uppercase() } }
        }
    }

    private val javaIdentCache = HashMap<String, String>()

    private fun javaIdent(kobolName: String): String =
        javaIdentCache.getOrPut(kobolName) {
            // CUSTOMER-NAME → customerName  (camelCase)
            val parts = kobolName.lowercase().split("-")
            parts[0] + parts.drop(1).joinToString("") { it.replaceFirstChar { c -> c.uppercase() } }
        }

    private fun f(name: String) = javaIdent(name)

    private fun kobolTypeToJava(type: KobolType): String = when (type) {
        is KobolType.IntegerType   -> "long"
        is KobolType.SmallIntType  -> "int"
        is KobolType.BooleanType   -> "boolean"
        is KobolType.DateType      -> "LocalDate"
        is KobolType.TimeType      -> "LocalTime"
        is KobolType.DateTimeType  -> "LocalDateTime"
        is KobolType.DecimalType   -> "BigDecimal"
        is KobolType.MoneyType     -> "BigDecimal"
        is KobolType.TextType      -> "String"
        is KobolType.ListType      -> "List<${kobolTypeToJava(type.elementType)}>"
        is KobolType.MapType       -> "Map<${kobolTypeToJava(type.keyType)}, ${kobolTypeToJava(type.valueType)}>"
        is KobolType.FutureType    -> "java.util.concurrent.CompletableFuture<${kobolTypeToJava(type.elementType)}>"
        is KobolType.RecordRefType  -> javaClass(type.name)
        is KobolType.VariantRefType  -> javaClass(type.name)
        is KobolType.JavaObjectType  -> "Object"
        is KobolType.VoidType        -> "void"
        is KobolType.UnknownType     -> "Object"
        is KobolType.UuidType        -> "java.util.UUID"
    }

    private fun defaultValue(type: KobolType): String = when (type) {
        is KobolType.IntegerType   -> "0L"
        is KobolType.SmallIntType  -> "0"
        is KobolType.BooleanType   -> "false"
        is KobolType.DecimalType,
        is KobolType.MoneyType     -> "BigDecimal.ZERO"
        is KobolType.TextType      -> "\"\""
        is KobolType.DateType      -> "LocalDate.now()"
        is KobolType.TimeType      -> "LocalTime.now()"
        is KobolType.DateTimeType  -> "LocalDateTime.now()"
        is KobolType.ListType      -> "new ArrayList<>()"
        is KobolType.MapType       -> "new LinkedHashMap<>()"
        is KobolType.RecordRefType -> "new ${javaClass(type.name)}()"
        else                       -> "null"
    }

    private fun escapeJavaString(s: String): String =
        s.replace("\\", "\\\\").replace("\"", "\\\"").replace("\n", "\\n").replace("\r", "\\r")

    private fun inferLiteralType(expr: Expression): KobolType = when (expr) {
        is Literal -> when (expr.kind) {
            LiteralKind.INTEGER -> KobolType.IntegerType
            LiteralKind.DECIMAL -> KobolType.DecimalType(10, 2)
            LiteralKind.STRING  -> KobolType.TextType(null)
            LiteralKind.BOOLEAN -> KobolType.BooleanType
        }
        is StringTemplateExpr -> KobolType.TextType(null)
        else -> checker.typeOf(expr).takeIf { it != KobolType.UnknownType } ?: KobolType.TextType(null)
    }

    companion object {
        private val MONGO_COMPARISON_OPS = setOf(
            BinaryOp.EQ, BinaryOp.NEQ, BinaryOp.LT, BinaryOp.GT, BinaryOp.LEQ, BinaryOp.GEQ
