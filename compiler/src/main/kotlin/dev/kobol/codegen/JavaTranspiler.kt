    private fun emitExpr(expr: Expression): String = when (expr) {
        is Literal -> when (expr.kind) {
            LiteralKind.INTEGER -> "${expr.value}L"
            LiteralKind.DECIMAL -> "new BigDecimal(\"${expr.value}\")"
            LiteralKind.STRING  -> "\"${escapeJavaString(expr.value.toString())}\""
            LiteralKind.BOOLEAN -> expr.value.toString()
        }

        is Reference -> emitRef(expr)

        is BinaryExpr -> emitBinary(expr)

        is UnaryExpr -> when (expr.op) {
            UnaryOp.NEGATE -> "(-${emitExpr(expr.operand)})"
            UnaryOp.NOT    -> "(!${emitExpr(expr.operand)})"
        }

        is StringTemplateExpr -> emitStringTemplate(expr)

        is BuiltinCall -> emitBuiltin(expr)

        is RecordLiteralExpr -> {
            val className = javaClass(expr.typeName)
            val args = expr.fields.joinToString(", ") { emitExpr(it.value) }
            // Use a builder pattern: create instance, then set fields
            val fieldSets = expr.fields.joinToString("; ") {
                "__rec_${javaIdent(expr.typeName)}.${javaIdent(it.name)} = ${emitExpr(it.value)}"
            }
            val varName = "__rec_${javaIdent(expr.typeName)}"
            // Inline block expression isn't valid Java — emit as a helper call
            // We'll use a lambda trick or just inline the assignments at statement level
            // For expression context, we need a different approach: allocate + init
            "new $className()"
        }

        is NamedArgument -> emitExpr(expr.value)

        is PipelineExpr  -> emitPipeline(expr)

        // F12 — dormant backend, best-effort only: emits `new <owner>(args)` but does NOT
        // resolve IMPORT aliases / stdlib paths to FQNs the way the ASM path does, so the alias
        // form won't compile as Java. Folded into F11 (port properly if the backend is revived).
        is NewExpr -> "new ${expr.owner}(${expr.args.joinToString(", ") { emitExpr(it) }})"

        // CALL in expression position (F14): emit the same `owner.method(args)` Java the CALL
        // statement emits — the dotted owner is a Java static class or an instance variable either way.
        is CallExpr -> "${expr.method}(${expr.args.joinToString(", ") { emitExpr(it) }})"

        // #v7 — list indexing. Dormant backend, best-effort: 1-based → 0-based List.get; the
        // element stays boxed (the ASM path unboxes/casts via castFromObject — ported if revived).
        is IndexExpr -> "${emitExpr(expr.target)}.get((int)(${emitExpr(expr.index)}) - 1)"
    }

    /**
     * Binary operators, type-aware.
     *
     * BigDecimal has no `==`/`+`/`-`/`*`/`/` operators in Java, and `==` on it is
     * reference equality (always wrong for value comparison). When either operand is
     * a DECIMAL/MONEY value we route to BigDecimal methods, mirroring [emitArith]:
     *   - comparisons → `compareTo(...)` (scale-insensitive: 1.50 == 1.5), and
     *   - arithmetic  → `add/subtract/multiply/divide`, honouring the active
     *     KobolMathContext so WITH PRECISION blocks apply.
     * Primitive operands keep the plain Java operators.
     */
    private fun emitBinary(expr: BinaryExpr): String {
        val decimal = (expr.op in DECIMAL_OPS) &&
            (isDecimalExpr(expr.left) || isDecimalExpr(expr.right) || expr.dividingMode != null)
        if (decimal) return emitDecimalBinary(expr)

        val l = emitExpr(expr.left)
        val r = emitExpr(expr.right)
        return when (expr.op) {
            BinaryOp.EQ       -> "($l == $r)"
            BinaryOp.NEQ      -> "($l != $r)"
            BinaryOp.LT       -> "($l < $r)"
            BinaryOp.GT       -> "($l > $r)"
            BinaryOp.LEQ      -> "($l <= $r)"
            BinaryOp.GEQ      -> "($l >= $r)"
            BinaryOp.AND      -> "($l && $r)"
            BinaryOp.OR       -> "($l || $r)"
            BinaryOp.ADD      -> "($l + $r)"
            BinaryOp.SUBTRACT -> "($l - $r)"
            BinaryOp.MULTIPLY -> "($l * $r)"
            BinaryOp.DIVIDE   -> "($l / $r)"
            BinaryOp.POWER    -> "(long)Math.pow((double)$l, (double)$r)"
        }
    }

    /** Emit a BigDecimal-typed binary op. Caller guarantees [expr.op] in [DECIMAL_OPS]. */
    private fun emitDecimalBinary(expr: BinaryExpr): String {
        val l = emitAsDecimal(expr.left)
        val r = emitAsDecimal(expr.right)
        val mc = "dev.kobol.stdlib.KobolMathContext.current()"
        return when (expr.op) {
            BinaryOp.EQ       -> "($l.compareTo($r) == 0)"
            BinaryOp.NEQ      -> "($l.compareTo($r) != 0)"
            BinaryOp.LT       -> "($l.compareTo($r) < 0)"
            BinaryOp.GT       -> "($l.compareTo($r) > 0)"
            BinaryOp.LEQ      -> "($l.compareTo($r) <= 0)"
            BinaryOp.GEQ      -> "($l.compareTo($r) >= 0)"
            BinaryOp.ADD      -> "($mc != null ? $l.add($r, $mc) : $l.add($r))"
            BinaryOp.SUBTRACT -> "($mc != null ? $l.subtract($r, $mc) : $l.subtract($r))"
            BinaryOp.MULTIPLY -> "($mc != null ? $l.multiply($r, $mc) : $l.multiply($r))"
            BinaryOp.DIVIDE   ->
                if (expr.dividingMode != null) "$l.divide($r, java.math.RoundingMode.${expr.dividingMode.replace('-', '_')})"
                else "($mc != null ? $l.divide($r, $mc) : $l.divide($r, java.math.RoundingMode.HALF_EVEN))"
            // BigDecimal.pow takes an int exponent; emit the raw (long) exponent cast to int.
            BinaryOp.POWER    -> "$l.pow((int)(${emitExpr(expr.right)}))"
            else -> error("emitDecimalBinary: ${expr.op} not a decimal op")
        }
    }

    /** True when [expr]'s static type is a fixed-point decimal (DECIMAL or MONEY). */
    private fun isDecimalExpr(expr: Expression): Boolean {
        // A DECIMAL literal already emits as `new BigDecimal(...)`; the checker does
        // not always tag bare literals, so recognise the literal kind directly.
        if (expr is Literal && expr.kind == LiteralKind.DECIMAL) return true
        return when (checker.typeOf(expr)) {
            is KobolType.DecimalType, is KobolType.MoneyType -> true
            else -> false
        }
    }

    /**
     * Emit [expr] as a BigDecimal. Decimal-typed exprs pass through; a primitive
     * operand in a mixed expression (e.g. `balance > 0`) is widened via
     * `BigDecimal.valueOf(...)` so `.compareTo`/`.add` type-check.
     */
    private fun emitAsDecimal(expr: Expression): String {
        val code = emitExpr(expr)
        return if (isDecimalExpr(expr)) code else "java.math.BigDecimal.valueOf($code)"
    }

    /**
     * Emit [expr] for assignment to a slot of type [target]. When the slot is a
     * decimal but the value is a primitive numeric (e.g. `total : DECIMAL = 0`),
     * widen to BigDecimal so the generated Java type-checks — otherwise pass through.
     */
    private fun emitExprFor(target: KobolType, expr: Expression): String =
        if ((target is KobolType.DecimalType || target is KobolType.MoneyType) && !isDecimalExpr(expr))
            emitAsDecimal(expr)
        else
            emitExpr(expr)

    /**
     * Render procedure-call arguments coerced to each declared parameter type — the Java twin of
     * AsmEmitter.emitProcArgs (G10). An INTEGER (`long`) argument into a DECIMAL/MONEY parameter is
     * widened to BigDecimal (else javac rejects `long`→BigDecimal); into a SMALLINT (`int`) slot it
     * is narrowed with `(int)`. `params` is null when the procedure can't be resolved (defensive) —
     * fall back to a bare emit, exactly as before. Same per-arg coercion as the ASM backend (P1).
     */
    private fun emitProcArgs(args: List<Expression>, params: List<Symbol.ProcedureSymbol.Param>?): String =
        args.mapIndexed { i, arg ->
            when (val p = params?.getOrNull(i)?.type) {
                is KobolType.DecimalType, is KobolType.MoneyType -> emitExprFor(p, arg)
                is KobolType.SmallIntType ->
                    if (checker.typeOf(arg) is KobolType.IntegerType) "(int)(${emitExpr(arg)})" else emitExpr(arg)
                else -> emitExpr(arg)
            }
        }.joinToString(", ")

    /**
     * Full static type of a reference, walking any field chain — so a record-field
     * target like `invoice.amount` resolves to the field's DECIMAL type, not the
     * record's. O(depth) in the number of `.`-separated parts.
     */
    private fun refType(ref: Reference): KobolType = checker.typeOfReference(ref)

    private fun emitStringTemplate(expr: StringTemplateExpr): String {
        if (expr.parts.isEmpty()) return "\"\""
        return expr.parts.joinToString(" + ") { part ->
            when (part) {
                is StringTemplatePart.RawText      -> "\"${escapeJavaString(part.text)}\""
                is StringTemplatePart.Interpolated -> "(${emitExpr(part.expr)})"
            }
        }
    }

    private fun emitBuiltin(expr: BuiltinCall): String {
        val args = expr.args.map { emitExpr(it) }
        return when (expr.name.uppercase()) {
            "COMBINE"   -> "KobolText.combine(${args.joinToString(", ")})"
            "UPPERCASE" -> "KobolText.uppercase(${args[0]})"
            "LOWERCASE" -> "KobolText.lowercase(${args[0]})"
            "TRIM"      -> "KobolText.trim(${args[0]})"
            "LENGTH"    -> "(long)KobolText.length(${args[0]})"
            "FIND"      -> "(long)KobolText.find(${args[0]}, ${args[1]})"
            "SUBSTRING" -> "KobolText.substring(${args[0]}, (int)${args[1]}, (int)${args[2]})"
            "REPLACE"   -> "KobolText.replace(${args[0]}, ${args[1]}, ${args[2]})"
            "REVERSE"   -> "KobolText.reverse(${args[0]})"
            "SPLIT"     -> when (args.size) {
                3    -> "KobolText.split(${args[0]}, ${args[1]}, (long)${args[2]})"
                else -> "KobolText.split(${args[0]}, ${args[1]})"
            }
            "ABS"       -> "KobolDecimal.abs(${args[0]})"
            "ROUND"     -> when (args.size) {
                3    -> "KobolDecimal.roundWithMode(${args[0]}, (int)${args[1]}, ${args[2]})"
                else -> "KobolDecimal.round(${args[0]}, (int)${args[1]})"
            }
            "ROUND-WITH-MODE" -> "KobolDecimal.roundWithMode(${args[0]}, (int)${args[1]}, ${args[2]})"
            "IS-BLANK"        -> "KobolText.isBlank(${args[0]})"
            "CONFIRM"         -> "dev.kobol.runtime.KobolEnv.confirm(${args[0]})"
            "IS-EMPTY"        -> "${args[0]}.isEmpty()"
            "IS-POSITIVE"     -> "dev.kobol.stdlib.KobolMath.isPositive(${args[0]})"
            "IS-NEGATIVE"     -> "dev.kobol.stdlib.KobolMath.isNegative(${args[0]})"
            "IS-ZERO"         -> "dev.kobol.stdlib.KobolMath.isZero(${args[0]})"
            "SCALE-OF"        -> "dev.kobol.stdlib.KobolMath.scaleOf(${args[0]})"
            "PRECISION-OF"    -> "dev.kobol.stdlib.KobolMath.precisionOf(${args[0]})"
            "UUID-GENERATE"   -> "dev.kobol.stdlib.KobolUuid.generate()"
            "UUID-NIL"        -> "dev.kobol.stdlib.KobolUuid.nil()"
            "UUID-FROM-TEXT"  -> "dev.kobol.stdlib.KobolUuid.fromText(${args[0]})"
            "UUID-TO-TEXT"    -> "dev.kobol.stdlib.KobolUuid.toText(${args[0]})"
            "TRUNCATE"        -> "KobolDecimal.truncate(${args[0]}, (int)${args[1]})"
            "MAX"       -> "KobolDecimal.max(${args[0]}, ${args[1]})"
            "MIN"       -> "KobolDecimal.min(${args[0]}, ${args[1]})"
            "DISPLAY_TABLE"       -> "KobolDisplay.table(${args[0]})"
            "DISPLAY_LABEL"       -> "KobolDisplay.label(${args[0]}, ${args[1]})"
            "DISPLAY_FORMAT"      -> "KobolDisplay.format(${args[0]}, ${args[1]})"
            "DISPLAY_JSON"        -> "dev.kobol.stdlib.KobolJson.toJson(${args[0]})"
            "DISPLAY_JSON_PRETTY" -> "dev.kobol.stdlib.KobolJson.toPrettyJson(${args[0]})"
            "DISPLAY_STYLED"      -> "dev.kobol.runtime.KobolDisplay.styled(${args[0]}, ${args[1]}, ${args[2]}, ${args[3]})"
            "DISPLAY_XML"         -> "dev.kobol.stdlib.KobolXml.toXml(${args[0]})"
            "DISPLAY_XML_PRETTY"  -> "dev.kobol.stdlib.KobolXml.toPrettyXml(${args[0]})"
            // User-defined procedure called in expression position (e.g. COMPUTE x = Fee(5)) — coerce
            // each arg to the declared param type so an INTEGER literal into a DECIMAL param widens
            // to BigDecimal, mirroring the PERFORM path (G10). Unresolved → bare emit, as before.
            else -> {
                val sym = checker.symbols.resolve(expr.name) as? Symbol.ProcedureSymbol
                "${expr.name}(${emitProcArgs(expr.args, sym?.params)})"
            }
        }
    }

    private fun emitPipeline(expr: PipelineExpr): String {
        // Emit as Java stream operations
        var chain = "${emitExpr(expr.source)}.stream()"
        for (stage in expr.stages) {
            chain += when (stage) {
                is PipelineStage.FilterStage    -> ".filter(__item -> ${emitExpr(stage.condition)})"
                is PipelineStage.TransformStage -> ".map(__item -> __item.${javaIdent(stage.field)})"
                is PipelineStage.SumStage       -> ".map(x -> x instanceof java.math.BigDecimal ? (java.math.BigDecimal)x : java.math.BigDecimal.valueOf(((Number)x).longValue())).reduce(java.math.BigDecimal.ZERO, java.math.BigDecimal::add)"
                is PipelineStage.SortStage      ->
                    if (stage.descending) ".sorted(java.util.Comparator.comparing(__item -> __item.${javaIdent(stage.field)}, java.util.Comparator.reverseOrder()))"
                    else                 ".sorted(java.util.Comparator.comparing(__item -> __item.${javaIdent(stage.field)}))"
                is PipelineStage.TakeStage      -> ".limit((long)(${emitExpr(stage.count)}))"
            }
        }
        // Terminate based on last stage
        val lastStage = expr.stages.lastOrNull()
        if (lastStage !is PipelineStage.SumStage) chain += ".collect(java.util.stream.Collectors.toList())"
        return chain
    }

    /** §27.1 ACCEPT — read raw String (terminal/argument), coerce to the target type, assign. */
    private fun emitAccept(stmt: AcceptStatement) {
        val name = stmt.target.parts.last()
        val raw = if (stmt.fromTerminal) {
            "dev.kobol.runtime.KobolEnv.acceptTerminal(${emitExpr(stmt.source)})"
        } else {
            val def = stmt.default?.let { "String.valueOf(${emitExpr(it)})" } ?: "\"\""
            "dev.kobol.runtime.KobolEnv.acceptArgument(${emitExpr(stmt.source)}, $def)"
        }
        val env = "dev.kobol.runtime.KobolEnv"
        val coerced = when (refType(stmt.target)) {
            is KobolType.IntegerType                         -> "$env.toLong($raw, \"$name\")"
            is KobolType.DecimalType, is KobolType.MoneyType -> "$env.toDecimal($raw, \"$name\")"
            is KobolType.BooleanType                         -> "$env.toBoolean($raw, \"$name\")"
            is KobolType.DateType                            -> "$env.toDate($raw, \"$name\")"
            is KobolType.TimeType                            -> "$env.toTime($raw, \"$name\")"
            is KobolType.DateTimeType                        -> "$env.toDateTime($raw, \"$name\")"
            else                                             -> raw   // TEXT / unknown
        }
        line("${emitRef(stmt.target)} = $coerced;")
    }

    private fun emitRef(ref: Reference): String {
        if (ref.parts.size == 1) {
            val namedCond = namedConditions[ref.parts[0]]
            if (namedCond != null) return "(${emitExpr(namedCond)})"
        }
        return ref.parts.joinToString(".") { javaIdent(it) }
    }

