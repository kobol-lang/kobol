package dev.kobol.codegen

import dev.kobol.parser.ast.*
import dev.kobol.semantic.KobolType
import dev.kobol.semantic.Symbol
import dev.kobol.semantic.TypeChecker

/**
 * Kobol AST → Java source code transpiler.
 *
 * Maps Kobol constructs to idiomatic Java:
 *   RECORD       → public static final class (POJO)
 *   DATA section → private static fields on the main class
 *   PROCEDURE    → private static method
 *   COMPUTE/LET  → Java assignment
 *   DISPLAY      → System.out.println(...)  with StringBuilder for interpolation
 *   STOP RUN     → System.exit(0)
 *   FOR EACH     → for (var x : list)
 *   WHILE        → while (cond)
 *   IF/ELSE      → if/else
 *   TRY/ON       → try/catch/finally
 *   CALL         → direct Java method invocation
 *
 * Ergonomics:
 *   String interpolation "{name}" → StringBuilder chain  (ER-4)
 *   Script mode (no PROGRAM header) → wraps in Main class  (ER-6)
 *   DISPLAY TABLE/LABEL/FORMAT     → KobolDisplay helpers  (ER-9)
 */
class JavaTranspiler(
    private val checker: TypeChecker,
    private val moduleRegistry: dev.kobol.semantic.ModuleRegistry = dev.kobol.semantic.ModuleRegistry(),
) {

    private val sb    = StringBuilder()
    private var indent = 0
    private val namedConditions = mutableMapOf<String, Expression>()
    private var currentClass = "Main"  // tracks the outer class name for method references

    /**
     * Transpile [program] to Java source.
     *
     * [className] is the name of the emitted top-level class. It defaults to a name
     * derived from `program.name`, but callers should pass the SAME name they use
     * for the output file (and the bytecode path), since Java requires a public
     * class to live in a file of the same name — otherwise `javac` rejects it.
     */
    fun transpile(program: Program, className: String = javaClass(program.name)): String {
        sb.clear(); indent = 0
        namedConditions.clear()
        for (cond in program.namedConditions) namedConditions[cond.name] = cond.expr

        line("// Generated by kobolc — do not edit")
        line("// Source program: ${program.name}")
        blank()
        line("import dev.kobol.runtime.*;")
        line("import java.math.BigDecimal;")
        line("import java.math.RoundingMode;")
        line("import java.util.*;")
        line("import java.time.*;")
        blank()

        line("public final class $className {")
        currentClass = className
        indent++

        // RECORD declarations → inner static classes
        for (rec in program.records) emitRecord(rec)

        // DATA section → static fields
        program.dataSection?.items?.forEach { emitDataField(it) }

        blank()

        // PROCEDURE declarations → static methods
        val exportedNames: Set<String> = program.moduleDecl?.exports?.map { it.name }?.toSet() ?: emptySet()
        for (proc in program.procedures) emitProcedure(proc, proc.exported || proc.name in exportedNames)

        // main() entry point — delegate to MAIN procedure
        val mainProc = program.procedures.find { it.name == "MAIN" }
        blank()
        line("public static void main(String[] args) {")
        indent++
        line("dev.kobol.runtime.KobolEnv.setArgs(args);")   // §27.1 ACCEPT FROM ARGUMENT
        if (mainProc != null) line("${javaIdent(mainProc.name)}();")
        else                  line("// No MAIN procedure found")
        indent--
        line("}")

        indent--
        line("}")
        return sb.toString()
    }

    // -------------------------------------------------------------------------
    // Records
    // -------------------------------------------------------------------------

    private fun emitRecord(rec: RecordDecl) {
        blank()
        line("public static final class ${javaClass(rec.name)} {")
        indent++
        for (f in rec.fields) {
            val jt = kobolTypeToJava(checker.toKobolType(f.type))
            line("public $jt ${javaIdent(f.name)} = ${defaultValue(checker.toKobolType(f.type))};")
        }
        // Simple toString for DISPLAY
        val fields = rec.fields.joinToString(" + \", \" + ") { "\"${f(it.name)}=\" + ${javaIdent(it.name)}" }
        blank()
        line("@Override public String toString() { return \"${rec.name}{\" + $fields + \"}\"; }")
        indent--
        line("}")
    }

    // -------------------------------------------------------------------------
    // Data fields
    // -------------------------------------------------------------------------

    private fun emitDataField(item: DataItem) {
        val kType = if (item.type != null) checker.toKobolType(item.type)
                    else item.initializer?.let { inferLiteralType(it) } ?: KobolType.UnknownType
        val jType = kobolTypeToJava(kType)
        val init  = if (item.initializer != null) " = ${emitExprFor(kType, item.initializer)}"
                    else " = ${defaultValue(kType)}"
        line("private static $jType ${javaIdent(item.name)}$init;")
    }

    // -------------------------------------------------------------------------
    // Procedures
    // -------------------------------------------------------------------------

    private fun emitProcedure(proc: ProcedureDecl, exported: Boolean = false) {
        blank()
        val retType = if (proc.isAsync) {
            val inner = if (proc.returnType != null) kobolTypeToJava(checker.toKobolType(proc.returnType)) else "Void"
            "java.util.concurrent.CompletableFuture<$inner>"
        } else if (proc.returnType != null) kobolTypeToJava(checker.toKobolType(proc.returnType))
        else "void"
        val params  = proc.params.joinToString(", ") {
            "${kobolTypeToJava(checker.toKobolType(it.type))} ${javaIdent(it.name)}"
        }
        val visibility = if (exported) "public" else "private"
        if (proc.isAsync) {
            line("$visibility static $retType ${javaIdent(proc.name)}($params) {")
            indent++
            line("return java.util.concurrent.CompletableFuture.supplyAsync(() -> {")
            indent++
            for (stmt in proc.body) emitStatement(stmt)
            if (proc.returnType == null) line("return null;")
            indent--
            line("}, dev.kobol.runtime.KobolConcurrent.INSTANCE);")
            indent--
            line("}")
        } else {
            line("$visibility static $retType ${javaIdent(proc.name)}($params) {")
            indent++
            for (stmt in proc.body) emitStatement(stmt)
            indent--
            line("}")
        }
    }

    // -------------------------------------------------------------------------
    // Statements
    // -------------------------------------------------------------------------

    private fun emitStatement(stmt: Statement) {
        when (stmt) {
            is MoveStatement     -> line("${emitRef(stmt.to)} = ${emitExprFor(refType(stmt.to), stmt.from)};")
            is AcceptStatement   -> emitAccept(stmt)
            is ComputeStatement  -> line("${emitRef(stmt.target)} = ${emitExprFor(refType(stmt.target), stmt.expr)};")
            is MapPutStatement   -> line("${emitRef(stmt.map)}.put(${emitExpr(stmt.key)}, ${emitExpr(stmt.value)});")
            is MapGetStatement   -> {
                // Cast to the boxed value type; assignment to a primitive target auto-unboxes (#12).
                val vt = (refType(stmt.map) as? KobolType.MapType)?.valueType ?: refType(stmt.into)
                val boxed = when (vt) {
                    is KobolType.IntegerType  -> "Long"
                    is KobolType.SmallIntType -> "Integer"
                    is KobolType.BooleanType  -> "Boolean"
                    else                      -> kobolTypeToJava(vt)
                }
                line("${emitRef(stmt.into)} = ($boxed) ${emitRef(stmt.map)}.get(${emitExpr(stmt.key)});")
            }
            is LocalVarDecl      -> {
                val kType = stmt.type?.let { checker.toKobolType(it) }
                            ?: inferLiteralType(stmt.initializer)
                line("${kobolTypeToJava(kType)} ${javaIdent(stmt.name)} = ${emitExprFor(kType, stmt.initializer)};")
            }
            is AddStatement      -> emitArith(stmt.target, "+", stmt.operand, stmt.giving)
            is SubtractStatement -> emitArith(stmt.from, "-", stmt.operand, stmt.giving)
            is MultiplyStatement -> emitArith(stmt.right, "*", stmt.left, stmt.giving)
            is DivideStatement   -> emitArith(stmt.into, "/", stmt.divisor, stmt.giving, stmt.dividingMode)
            is DisplayStatement  -> emitDisplay(stmt)
            is PerformStatement  -> {
                if (stmt.moduleAlias != null) {
                    val mod = moduleRegistry.resolveByAlias(stmt.moduleAlias)
                        ?: error("Unknown module alias '${stmt.moduleAlias}' at transpile time")
                    val proc = mod.procedures[stmt.procedureName.uppercase()]
                    val args = emitProcArgs(stmt.args, proc?.params)
                    line("${mod.jvmClassName}.${javaIdent(stmt.procedureName)}($args);")
                } else {
                    val sym  = checker.symbols.resolve(stmt.procedureName) as? Symbol.ProcedureSymbol
                    val args = emitProcArgs(stmt.args, sym?.params)
                    line("${javaIdent(stmt.procedureName)}($args);")
                }
            }
            is CallStatement     -> {
                val method = stmt.method.split(".").joinToString(".")
                val args   = stmt.args.joinToString(", ") { emitExpr(it) }
                val call   = "$method($args)"
                if (stmt.giving != null) line("${emitRef(stmt.giving)} = $call;")
                else line("$call;")
            }
            is IfStatement       -> emitIf(stmt)
            is WhileStatement    -> {
                line("while (${emitExpr(stmt.condition)}) {")
                indent++; stmt.body.forEach { emitStatement(it) }; indent--
                line("}")
            }
            is ForEachStatement  -> {
                val iterType = checker.typeOf(stmt.iterable)
                val elemJava = when (iterType) {
                    is KobolType.ListType -> kobolTypeToJava(iterType.elementType)
                    else                  -> "var"
                }
                line("for ($elemJava ${javaIdent(stmt.variable)} : ${emitExpr(stmt.iterable)}) {")
                indent++; stmt.body.forEach { emitStatement(it) }; indent--
                line("}")
            }
            is RepeatStatement   -> {
                line("for (long __repeat = 0, __limit = (long)(${emitExpr(stmt.count)}); __repeat < __limit; __repeat++) {")
                indent++; stmt.body.forEach { emitStatement(it) }; indent--
                line("}")
            }
            is OpenStatement     -> line("/* OPEN ${stmt.fileName} FOR ${stmt.mode} */")
            is ReadStatement     -> line("/* READ ${stmt.fileName} */")
            is WriteStatement    -> line("/* WRITE ${stmt.fileName} */")
            is CloseStatement    -> line("/* CLOSE ${stmt.fileName} */")
            is WriteJsonStatement -> {
                val method = if (stmt.pretty) "writePrettyToFile" else "writeToFile"
                line("dev.kobol.stdlib.KobolJson.$method(${emitExpr(stmt.value)}, ${emitExpr(stmt.filepath)});")
            }
            is WriteXmlStatement -> {
                if (stmt.rootName != null) {
                    line("dev.kobol.stdlib.KobolXml.writeWithRoot(${emitExpr(stmt.value)}, ${emitExpr(stmt.filepath)}, ${emitExpr(stmt.rootName)}, ${stmt.pretty});")
                } else {
                    val method = if (stmt.pretty) "writePrettyToFile" else "writeToFile"
                    line("dev.kobol.stdlib.KobolXml.$method(${emitExpr(stmt.value)}, ${emitExpr(stmt.filepath)});")
                }
            }
            is ParseJsonStatement -> {
                if (stmt.asList) {
                    val method = if (stmt.fromFile) "parseFileArray" else "parseJsonArray"
                    line("${emitRef(stmt.into)} = dev.kobol.stdlib.KobolJson.$method(${emitExpr(stmt.source)});")
                } else {
                    val method = if (stmt.fromFile) "parseFileInto" else "parseInto"
                    line("dev.kobol.stdlib.KobolJson.$method(${emitRef(stmt.into)}, ${emitExpr(stmt.source)});")
                }
            }
            is ParseXmlStatement -> {
                if (stmt.asList) {
                    val method = if (stmt.fromFile) "parseFileElements" else "parseXmlElements"
                    line("${emitRef(stmt.into)} = dev.kobol.stdlib.KobolXml.$method(${emitExpr(stmt.source)});")
                } else if (stmt.namespaces.isNotEmpty()) {
                    // §30.3 — validate the root namespace against the declared URIs.
                    val uris   = stmt.namespaces.joinToString(", ") { emitExpr(it.second) }
                    val method = if (stmt.fromFile) "parseFileIntoNamespaces" else "parseIntoNamespaces"
                    line("dev.kobol.stdlib.KobolXml.$method(${emitRef(stmt.into)}, ${emitExpr(stmt.source)}, new String[]{$uris});")
                } else {
                    val method = if (stmt.fromFile) "parseFileInto" else "parseInto"
                    line("dev.kobol.stdlib.KobolXml.$method(${emitRef(stmt.into)}, ${emitExpr(stmt.source)});")
                }
            }
            is WithPrecisionStatement -> {
                if (stmt.roundingMode != null) {
                    line("dev.kobol.stdlib.KobolMathContext.pushWithRounding(\"${stmt.precisionName}\", \"${stmt.roundingMode}\");")
                } else {
                    line("dev.kobol.stdlib.KobolMathContext.push(\"${stmt.precisionName}\");")
                }
                line("try {")
                indent++; stmt.body.forEach { emitStatement(it) }; indent--
                line("} finally {")
                line("    dev.kobol.stdlib.KobolMathContext.pop();")
                line("}")
            }
            is RoundStatement -> {
                val scale = emitExpr(stmt.scale)
                val target = emitRef(stmt.target)
                if (stmt.mode != null) {
                    line("$target = dev.kobol.stdlib.KobolMath.roundWithMode($target, $scale, \"${stmt.mode}\");")
                } else {
                    line("$target = dev.kobol.stdlib.KobolMath.round($target, $scale);")
                }
            }
            is TryStatement      -> emitTry(stmt)
            is RaiseStatement    -> {
                val msg = stmt.message?.let { emitExpr(it) } ?: "\"${stmt.exceptionType}\""
                line("throw new KobolException($msg);")
            }
            is ReturnStatement   -> {
                if (stmt.value != null) line("return ${emitExpr(stmt.value)};")
                else line("return;")
            }
            is StopRunStatement  -> {
                val code = stmt.exitCode?.let { emitExpr(it) } ?: "0"
                line("System.exit((int)($code));")
            }
            // Logging and concurrency statements — stub out for Java transpiler
            is LogStatement             -> line("/* LOG ${stmt.level} */")
            is ConcurrentBlock          -> line("/* CONCURRENT block (${stmt.branches.size} branches) */")
            is ParallelForEachStatement -> line("/* PARALLEL FOR EACH ${stmt.variable} */")
            is ValidateStatement        -> line("/* VALIDATE */")
            is MatchStatement           -> line("/* MATCH */")
            is SleepStatement -> {
                val factor = when (stmt.unit) {
                    SleepUnit.MILLISECONDS -> 1L
                    SleepUnit.SECONDS      -> 1000L
                    SleepUnit.MINUTES      -> 60_000L
                }
                val millis = if (factor == 1L) emitExpr(stmt.amount) else "(${emitExpr(stmt.amount)} * ${factor}L)"
                line("try { Thread.sleep($millis); } catch (InterruptedException __e) { Thread.currentThread().interrupt(); }")
            }
            is AssertStatement -> {
                val cond = emitExpr(stmt.condition)
                if (stmt.message != null) {
                    line("dev.kobol.runtime.KobolTest.assertTrue($cond, ${emitExpr(stmt.message)});")
                } else {
                    line("dev.kobol.runtime.KobolTest.assertTrue($cond);")
                }
            }
            is AssertRaisesStatement -> {
                // Catch class via the SHARED resolver; '/'+'$' → '.' for Java source form.
                val exClass = kobolExceptionJvmName(stmt.exceptionType).replace('/', '.').replace('$', '.')
                line("{ // ASSERT RAISES ${stmt.exceptionType}  (own scope so __raised never clashes)")
                indent++
                line("boolean __raised = false;")
                line("try {")
                indent++; emitStatement(stmt.body); indent--
                line("} catch ($exClass __e) { __raised = true; }")
                line("dev.kobol.runtime.KobolTest.assertTrue(__raised, \"Expected ${stmt.exceptionType} to be raised\");")
                indent--
                line("}")
            }
            is MockStatement -> {
                line("dev.kobol.runtime.KobolMockRegistry.register(\"${stmt.procedureName}\", ${emitExpr(stmt.returns)});")
            }

            // ── Group 11: HTTP client ─────────────────────────────────────────
            is HttpCallStatement -> {
                val url      = emitExpr(stmt.url)
                val headers  = if (stmt.headers  != null) emitExpr(stmt.headers)  else "null"
                val timeout  = if (stmt.timeout  != null) emitExpr(stmt.timeout)  else "30L"
                val HTTP     = "dev.kobol.stdlib.KobolHttp"
                val call = when (stmt.httpMethod) {
                    "GET", "DELETE" -> "$HTTP.${stmt.httpMethod.lowercase()}($url, $headers, $timeout)"
                    else -> {
                        val body = if (stmt.body != null) emitExpr(stmt.body) else "null"
                        "$HTTP.${stmt.httpMethod.lowercase()}($url, $headers, $body, $timeout)"
                    }
                }
                if (stmt.giving != null) line("${emitRef(stmt.giving)} = $call;")
                else line("$call;")
            }

            // ── Group 11: JDBC bridge ─────────────────────────────────────────
            is JdbcConnectStatement -> {
                val url  = emitExpr(stmt.url)
                val user = if (stmt.user     != null) emitExpr(stmt.user)     else "null"
                val pw   = if (stmt.password != null) emitExpr(stmt.password) else "null"
                line("dev.kobol.stdlib.KobolJdbc.connect($url, $user, $pw);")
            }
            is JdbcQueryStatement -> {
                val sql    = emitExpr(stmt.sql)
                val params = "new Object[]{ ${stmt.params.joinToString { emitExpr(it) }} }"
                val call   = "dev.kobol.stdlib.KobolJdbc.query($sql, $params)"
                if (stmt.into != null) line("${emitRef(stmt.into)} = $call;")
                else line("$call;")
            }
            is JdbcExecuteStatement -> {
                val sql    = emitExpr(stmt.sql)
                val params = "new Object[]{ ${stmt.params.joinToString { emitExpr(it) }} }"
                line("dev.kobol.stdlib.KobolJdbc.execute($sql, $params);")
            }
            is JdbcDisconnectStatement -> line("dev.kobol.stdlib.KobolJdbc.disconnect();")

            // ── Group 12: REST server ─────────────────────────────────────────
            is ServerStatement -> {
                val SRV = "dev.kobol.stdlib.KobolServer"
                for (ep in stmt.endpoints) {
                    // Emit endpoint as a local lambda capturing nothing
                    val bodyCode = buildString {
                        for (s in ep.body) {
                            // Inline transpile statements into lambda — simplified
                            append("// endpoint body — see generated class\n")
                        }
                    }
                    // Generate a static helper method name and register
                    val safePath = ep.path.replace(Regex("[^A-Za-z0-9_]"), "_")
                    val helperName = "__endpoint_${ep.method}_$safePath"
                    line("$SRV.register(\"${ep.method}\", \"${ep.path}\", ${currentClass}::$helperName);")
                }
                line("$SRV.start((int)(${emitExpr(stmt.port)}));")
                // Emit helper methods
                for (ep in stmt.endpoints) {
                    val safePath = ep.path.replace(Regex("[^A-Za-z0-9_]"), "_")
                    val helperName = "__endpoint_${ep.method}_$safePath"
                    emitEndpointHelper(helperName, ep)
                }
            }
            is RespondStatement -> {
                val SRV  = "dev.kobol.stdlib.KobolServer"
                val JSON = "dev.kobol.stdlib.KobolJson"
                if (stmt.statusCode != null) {
                    line("$SRV.respondStatus((int)(${emitExpr(stmt.statusCode)}));")
                }
                val value = if (stmt.asJson) "$JSON.toJson(${emitExpr(stmt.value)})" else emitExpr(stmt.value)
                line("return $value;")
            }
            is AwaitStatement -> {
                line("${emitRef(stmt.into)} = (${kobolTypeToJava(checker.typeOf(stmt.into))}) ${emitRef(stmt.future)}.join();")
            }

            // ── Group 13: NoSQL document store ──────────────────────────────
            is NoSqlConnectStatement    -> line("dev.kobol.stdlib.KobolMongo.connect(${emitExpr(stmt.url)}, ${emitExpr(stmt.database)});")
            is NoSqlDisconnectStatement -> line("dev.kobol.stdlib.KobolMongo.disconnect();")
            is NoSqlFindStatement       -> {
                val filter = if (stmt.filter != null) emitMongoFilterJava(stmt.filter) else "null"
                val call = if (stmt.findOne)
                    "dev.kobol.stdlib.KobolMongo.findOne(${emitExpr(stmt.collection)}, $filter)"
                else
                    "dev.kobol.stdlib.KobolMongo.find(${emitExpr(stmt.collection)}, $filter)"
                line("${emitRef(stmt.giving)} = $call;")
            }
            is NoSqlSaveStatement       -> {
                if (stmt.upsert) {
                    line("dev.kobol.stdlib.KobolMongo.upsert(${emitExpr(stmt.collection)}, null, ${emitRef(stmt.document)});")
                } else {
                    line("dev.kobol.stdlib.KobolMongo.save(${emitExpr(stmt.collection)}, ${emitRef(stmt.document)});")
                }
            }
            is NoSqlDeleteStatement     -> line("dev.kobol.stdlib.KobolMongo.delete(${emitExpr(stmt.collection)}, ${emitMongoFilterJava(stmt.filter)});")
            is NoSqlCountStatement      -> {
                val filter = if (stmt.filter != null) emitMongoFilterJava(stmt.filter) else "null"
                line("${emitRef(stmt.giving)} = dev.kobol.stdlib.KobolMongo.count(${emitExpr(stmt.collection)}, $filter);")
            }

            // ── Group 14: Cache / key-value store ───────────────────────────
            is CacheConnectStatement    -> line("dev.kobol.stdlib.KobolRedis.connect(${emitExpr(stmt.url)});")
            is CacheDisconnectStatement -> line("dev.kobol.stdlib.KobolRedis.disconnect();")
            is CacheGetStatement        -> line("${emitRef(stmt.giving)} = dev.kobol.stdlib.KobolRedis.get(${emitExpr(stmt.key)});")
            is CacheSetStatement        -> {
                if (stmt.ttlSeconds != null) {
                    line("dev.kobol.stdlib.KobolRedis.setEx(${emitExpr(stmt.key)}, ${emitExpr(stmt.value)}, ${emitExpr(stmt.ttlSeconds)});")
                } else {
                    line("dev.kobol.stdlib.KobolRedis.set(${emitExpr(stmt.key)}, ${emitExpr(stmt.value)});")
                }
            }
            is CacheDeleteStatement     -> line("dev.kobol.stdlib.KobolRedis.delete(${emitExpr(stmt.key)});")
            is CacheExistsStatement     -> line("${emitRef(stmt.giving)} = dev.kobol.stdlib.KobolRedis.exists(${emitExpr(stmt.key)});")
        }
    }

    /** Emit a Java expression string representing a MongoDB Bson filter. */
    private fun emitMongoFilterJava(expr: Expression): String {
        val F = "com.mongodb.client.model.Filters"
        return when {
            expr is BinaryExpr && expr.op == BinaryOp.AND ->
                "$F.and($F.and(${emitMongoFilterJava(expr.left)}, ${emitMongoFilterJava(expr.right)}))"
            expr is BinaryExpr && expr.op == BinaryOp.OR ->
                "$F.or($F.or(${emitMongoFilterJava(expr.left)}, ${emitMongoFilterJava(expr.right)}))"
            expr is BinaryExpr && expr.op in MONGO_COMPARISON_OPS -> {
                val field = if (expr.left is Reference) (expr.left as Reference).parts.joinToString(".") else "value"
                val value = emitExpr(expr.right)
                val method = when (expr.op) {
                    BinaryOp.EQ  -> "eq"
                    BinaryOp.NEQ -> "ne"
                    BinaryOp.LT  -> "lt"
                    BinaryOp.GT  -> "gt"
                    BinaryOp.LEQ -> "lte"
                    BinaryOp.GEQ -> "gte"
                    else         -> "eq"
                }
                "$F.$method(\"$field\", $value)"
            }
            else -> "null"
        }
    }

    private fun emitEndpointHelper(name: String, ep: EndpointHandler) {
        val SRV = "dev.kobol.stdlib.KobolServer"
        blank()
        line("private static String $name(String body) {")
        indent++
        // Initialise path-parameter locals
        for (param in ep.pathParams) {
            line("String $param = $SRV.currentPathParam(\"$param\");")
        }
        for (s in ep.body) emitStatement(s)
        line("return \"\"; // fallthrough")
        indent--
        line("}")
    }

    private fun emitArith(target: Reference, op: String, operand: Expression, giving: Reference?, dividingMode: String? = null) {
        val lhs = emitRef(target)
        val rhs = emitExpr(operand)
        val dest = if (giving != null) emitRef(giving) else lhs
        // Resolve the full operand type (field chains included) so a record-field
        // money/decimal operand like `invoice.amount` takes the BigDecimal
        // arithmetic path, not `+`/`-`. `target` is the lhs operand here.
        val kType = refType(target)
        if (kType is KobolType.MoneyType || kType is KobolType.DecimalType) {
            val javOp = when (op) {
                "+" -> "add"; "-" -> "subtract"; "*" -> "multiply"; "/" -> "divide"
                else -> op
            }
            val mc = "dev.kobol.stdlib.KobolMathContext.current()"
            if (op == "/") {
                val roundArg = dividingMode ?: "HALF_EVEN"
                line("$dest = ($mc != null) ? $lhs.${javOp}($rhs, $mc) : $lhs.${javOp}($rhs, java.math.RoundingMode.$roundArg);")
            } else {
                line("$dest = ($mc != null) ? $lhs.${javOp}($rhs, $mc) : $lhs.${javOp}($rhs);")
            }
        } else {
            line("$dest = $lhs $op $rhs;")
        }
    }

    private fun emitDisplay(stmt: DisplayStatement) {
        // §27.2 DISPLAY PROGRESS renders in-place to stderr (void), not via println.
        val only = stmt.values.singleOrNull()
        if (only is BuiltinCall && only.name == "DISPLAY_PROGRESS") {
            line("dev.kobol.runtime.KobolDisplay.progress((long)(${emitExpr(only.args[0])}), (long)(${emitExpr(only.args[1])}), ${emitExpr(only.args[2])});")
            return
        }
        val arg = stmt.values.joinToString(" + \" \" + ") { emitDisplayArg(it) }
        line("System.out.println($arg);")
    }

    private fun emitDisplayArg(expr: Expression): String = when (expr) {
        is BuiltinCall -> when (expr.name) {
            "DISPLAY_TABLE"       -> "KobolDisplay.table(${emitExpr(expr.args[0])})"
            "DISPLAY_LABEL"       -> "KobolDisplay.label(${emitExpr(expr.args[0])}, ${emitExpr(expr.args[1])})"
            "DISPLAY_FORMAT"      -> "KobolDisplay.format(${emitExpr(expr.args[0])}, ${emitExpr(expr.args[1])})"
            "DISPLAY_JSON"        -> "dev.kobol.stdlib.KobolJson.toJson(${emitExpr(expr.args[0])})"
            "DISPLAY_JSON_PRETTY" -> "dev.kobol.stdlib.KobolJson.toPrettyJson(${emitExpr(expr.args[0])})"
            "DISPLAY_STYLED"      -> "dev.kobol.runtime.KobolDisplay.styled(${emitExpr(expr.args[0])}, ${emitExpr(expr.args[1])}, ${emitExpr(expr.args[2])}, ${emitExpr(expr.args[3])})"
            "DISPLAY_XML"         -> "dev.kobol.stdlib.KobolXml.toXml(${emitExpr(expr.args[0])})"
            "DISPLAY_XML_PRETTY"  -> "dev.kobol.stdlib.KobolXml.toPrettyXml(${emitExpr(expr.args[0])})"
            else                  -> emitExpr(expr)
        }
        else -> emitExpr(expr)
    }

    private fun emitIf(stmt: IfStatement) {
        line("if (${emitExpr(stmt.condition)}) {")
        indent++; stmt.thenBranch.forEach { emitStatement(it) }; indent--
        for (ei in stmt.elseIfClauses) {
            line("} else if (${emitExpr(ei.condition)}) {")
            indent++; ei.body.forEach { emitStatement(it) }; indent--
        }
        if (stmt.elseBranch != null) {
            line("} else {")
            indent++; stmt.elseBranch.forEach { emitStatement(it) }; indent--
        }
        line("}")
    }

    private fun emitTry(stmt: TryStatement) {
        line("try {")
        indent++; stmt.body.forEach { emitStatement(it) }; indent--
        for (h in stmt.handlers) {
            val binding = h.binding?.let { javaIdent(it) } ?: "__ex"
            line("} catch (Exception $binding) { // ON ${h.exceptionType}")
            indent++; h.body.forEach { emitStatement(it) }; indent--
        }
        if (stmt.ensure != null) {
            line("} finally {")
            indent++; stmt.ensure.forEach { emitStatement(it) }; indent--
        }
        line("}")
    }

